package org.apache.kafka.clients.producer;

/**
 * Objects of classes that implement the ProducerState interface
 * are returned to the application so that the application
 * can checkpoint its state along with their own state to guarantee
 * recovery with a state consistent state across application and
 * idempotent producer.
 */
public interface ProducerState {
    public byte[] getBytes();
}
